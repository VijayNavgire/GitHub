package PRACTICE;

public class Method_Non_Static_Data 
{
	public void Ar()
	{
		int area;
		area = 6 * 6;
		System.out.println("This is Non-Static Data Method");
		System.out.println("Area = "+ area);
	}
	
	public void Sq()
	{
		int square;
		square = 15 * 15;
		System.out.println("Square = "+ square);
	}

	

}
