package PRACTICE;

public class IF_Condition 
{
	public static void main(String[] args) 
	{
		int mark = 25;
		
		if (mark >= 35)
		{
			System.out.println("Result = " + mark + " PASS");
		}
		
		if (mark <= 35)
		{
		System.out.println("Result = " + mark + " FAIL");
		}
		
		
		
	}

}
