package Polymorphism;

public class Meth_Overloading 
{
	public void add(int a, int b) // 2 parameter
	{
		System.out.println("Addition of two no. = "+ (a+b));
	}
	//
	public void add(int a, int b, int c) // 3 parameter
	{
		System.out.println("Addition of three no. = "+ (a+b+c));
	}
	

}
