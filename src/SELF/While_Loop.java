package SELF;

public class While_Loop 
{
	public static void main(String[] args) 
	{
		int i=2;
		while (i<=5)
		{
			System.out.println(i);
			i++;
		}
		System.out.println("----------");
		
		int p =2;
		do 
		{
			System.out.println(p);
			p++;
		}
		while (p<=5);
		
		
		System.out.println("-----------------------------");
		
		for(int j = 1; j<=10; j++)
		{
			System.out.println(j);
			
		}
		System.out.println("=====================");
	}
	

}
